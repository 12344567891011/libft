void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*p;
	size_t		i;

	p = s;
	i = 0;
	while (p[i] != '\0' && i < n)
	{
		if (p[i] == c)
		{
			break ;
		}
		i++;
	}
	return ((char *)s + i);
}
