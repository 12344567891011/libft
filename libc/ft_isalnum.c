#include<stdio.h>
int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 121) || (c >= 48 && c <= 57))
	return(1);
	else
	return(0);
}

int main ()
{
	char c;
	c = '1';
	printf("%d", ft_isalnum(c));
}